//diem: gioi, kha, tb, yeu
const diem = 8;
switch (diem) {
    case (diem >= 8 && diem <=10):
        console.log("Gioi")
        break;
    case (diem >= 6 && diem <=7):
        console.log("Kha")
        break;
    case 5:
        console.log("TB")
        break;
    default:
        console.log("Yeu")
}